/**
 * Get the current month name in the specified language
 */
export function getCurrentMonth(lang: string = 'en'): string {
  const months = monthNames[lang] || monthNames.en;
  return months[new Date().getMonth()];
}

/**
 * Get the current year
 */
export function getCurrentYear(): number {
  return new Date().getFullYear();
}

/**
 * Get formatted "Month Year" string in the specified language
 */
export function getMonthYear(lang: string = 'en'): string {
  return `${getCurrentMonth(lang)} ${getCurrentYear()}`;
}

/**
 * Get "Verified & Working - Month Year" in the specified language
 */
export function getVerifiedBadgeText(lang: string = 'en', prefix?: string): string {
  const defaultPrefix = prefix || 'Verified & Working';
  return `${defaultPrefix} - ${getMonthYear(lang)}`;
}
